package lecture2;

public class Employee {
	
	private String FirstName;
	private String LastName;
	
	public Employee(String firstName, String lastName) {
		FirstName = firstName;
		LastName = lastName;
	}
	
	public String getFirstName() {
		return FirstName;
	}
	
	public String getLastName() {
		return LastName;
	}
	
}
